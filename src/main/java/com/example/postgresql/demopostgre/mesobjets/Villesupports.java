package com.example.postgresql.demopostgre.mesobjets;

public class Villesupports {

    String ville, typesupport;
    int idsupport, quantite, idvil;

    public Villesupports() {
    }

    public String getVille() {
        return ville;
    }

    public void setVille(String ville) {
        this.ville = ville;
    }

    public String getTypesupport() {
        return typesupport;
    }

    public void setTypesupport(String typesupport) {
        this.typesupport = typesupport;
    }

    public int getIdsupport() {
        return idsupport;
    }

    public void setIdsupport(int idsupport) {
        this.idsupport = idsupport;
    }

    public int getQuantite() {
        return quantite;
    }

    public void setQuantite(int quantite) {
        this.quantite = quantite;
    }

    public int getIdvil() {
        return idvil;
    }

    public void setIdvil(int idvil) {
        this.idvil = idvil;
    }
}
